public class RedheadDuck extends Duck implements Quackable , Flyable {
    public void display() {
        System.out.println("i am a RedheadDuck !");
    }
    public void quack() {
        System.out.println("RedheadDuck quack quack");
    }
    public void fly() {
        System.out.println("I am a flying RedheadDuck");
    }
}
